def get_stock_name(ticker_str):
    """從模組化字典中查詢中文名稱，絕不顯示英文"""
    clean_ticker = str(ticker_str).strip().upper().replace('.TW', '').replace('.TWO', '')

    if clean_ticker in TW_STOCK_NAMES:
        return f"{clean_ticker} {TW_STOCK_NAMES[clean_ticker]}"

    return clean_ticker